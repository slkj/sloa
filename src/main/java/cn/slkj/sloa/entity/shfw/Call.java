package cn.slkj.sloa.entity.shfw;

/**
 * 客服来电
 * 
 * @author xiaotianliang
 *
 */
public class Call {
	private String id;
	private String askName;// 来电者姓名
	private String number;// 来电号码
	private String department;// 来电号码所属部门
	private String callTime;// 来电时间
	private String conversation;// 通话内容
	private String answerName;// 接听者姓名
	private String transferName;// 转接员姓名

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getAskName() {
		return askName;
	}

	public void setAskName(String askName) {
		this.askName = askName;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getDepartment() {
		return department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public String getCallTime() {
		return callTime;
	}

	public void setCallTime(String callTime) {
		this.callTime = callTime;
	}

	public String getConversation() {
		return conversation;
	}

	public void setConversation(String conversation) {
		this.conversation = conversation;
	}

	public String getAnswerName() {
		return answerName;
	}

	public void setAnswerName(String answerName) {
		this.answerName = answerName;
	}

	public String getTransferName() {
		return transferName;
	}

	public void setTransferName(String transferName) {
		this.transferName = transferName;
	}

	@Override
	public String toString() {
		return "Call [id=" + id + ",askName=" + askName + "," + "number="
				+ number + ",department=" + department + "," + "callTime="
				+ callTime + ",conversation=" + conversation + ","
				+ "answerName=" + answerName + ",transferName=" + transferName
				+ "" + "]";
	}
}
